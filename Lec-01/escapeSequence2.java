public class escapeSequence2 {
    public static void main(String[] args) {
        System.out.println("helloshiva...");
        System.out.println("hello\"shiva\"...");
        System.out.println("hello\'shiva\''...");
        System.out.println("hello\nshiva...");
        System.out.println("hello\tshiva...");
        System.out.println("hello\bshiva...");
    }
    
}
